use std::iter::FromIterator;

#[fastout]
fn main() {
    input! {
        mut h: isize,
    }

    let mut i = 0;

    while h >= 0 {
        h -= 2isize.pow(i as u32);

        i += 1
    }

    print!("{}", i);
}
